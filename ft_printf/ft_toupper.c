#include "ft_printf.h"

char	*ft_toupper(char *ch)
{
	int	i;
	int	y;

	i = 0;
	y = 12;
	while (ch[i])
	{
		if (ch[i] >= 'a' && ch[i] <= 'z')
			ch[i] -= 32;
		i++;
	}
	return (ch);
}
